CREATE TABLE USUARIO_JAVA (
  ID     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  NOME   VARCHAR2(100 CHAR) NOT NULL,
  EMAIL  VARCHAR2(200 CHAR) NOT NULL,
  SENHA  VARCHAR2(120 CHAR) NOT NULL,
  ROLE   VARCHAR2(30 CHAR)  NOT NULL,
  CONSTRAINT UK_USUARIOS_EMAIL UNIQUE (EMAIL),
  CONSTRAINT CK_USUARIOS_ROLE CHECK (ROLE IN ('GERENCIA_VAGA','GERENCIA_MOTO'))
);
