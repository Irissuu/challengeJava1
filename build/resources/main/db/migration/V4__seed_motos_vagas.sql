CREATE TABLE VAGA_MOTO (
   VAGA_ID NUMBER NOT NULL,
   MOTO_ID NUMBER NOT NULL,
   CONSTRAINT PK_VAGA_MOTO PRIMARY KEY (VAGA_ID, MOTO_ID),
   CONSTRAINT FK_VAGA_MOTO_VAGA FOREIGN KEY (VAGA_ID)
       REFERENCES VAGA_JAVA (ID) ON DELETE CASCADE,
   CONSTRAINT FK_VAGA_MOTO_MOTO FOREIGN KEY (MOTO_ID)
       REFERENCES MOTO_JAVA (ID) ON DELETE CASCADE
);
